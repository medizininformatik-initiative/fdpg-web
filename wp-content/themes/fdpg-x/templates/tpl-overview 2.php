              Ziel 3: Validierung
              der entwickelten Modelle <br> Die entwickelten Modelle sollen auf
              historischen Daten ab 2015 trainiert werden und auf dem letzten
              verf&uuml;gbaren Jahr im Datenintegrationszentrum getestet bzw.
              validiert werden. <br> <br>
                F&uuml;r das Universit&auml;tsklinikum Mannheim (MIRACUM)
              haben wir bereits gezeigt, dass mit Hilfe von &sect; 21-Daten und
              datenschutzkonformen Matching zu den Wetterdaten des Deutschen
              Wetterdienstes (DWD) derartige Modellentwicklung und Vorhersage
              m&ouml;glich ist.
