            <h3>Projektbeschreibung</h3>
            <p class="big">
              Hitzewellen f&uuml;hren bekannterma&szlig;en zu einer H&auml;ufung von
              Krankenhauseinweisungen. Im vorliegenden Projekt soll untersucht
              werden, wie gro&szlig; der Einfluss der Hitze auf das Schlaganfallrisiko
              <a target="_blank" href="https://www.gesundheitsinformation.de/schlaganfall.html">(https://www.gesundheitsinformation.de/schlaganfall.html)</a>
              einer Person ist. Daf&uuml;r wird im ersten Schritt ein Basisrisiko
              ermittelt, das Patientinnen und Patienten allgemein und in
              Abh&auml;ngigkeit von ihrem Gesundheitszustand haben. Im zweiten
              Schritt wird ermittelt, wie sehr das Schlaganfallrisiko durch eine
              Hitzewelle in einer Region zunimmt. Daf&uuml;r werden regionale Daten
              des Deutschen Wetterdienstes verwendet und gemeinsam mit den Daten
              zu Aufnahmen mit der Diagnose Schlaganfall aus vielen
              teilnehmenden Unikliniken analysiert.
            </p>
            <h3>Projektbeschreibung</h3>
            <h6>Projektziele:</h6>
            <br>
            <p class="big">
              Ziel 1: Basisrisiko f&uuml;r Schlaganfall <br>
               Modellierung des Basisrisikos, dass in der lokalen Population (zum Beispiel
              Standort und Einzugsgebiet) Schlaganf&auml;lle auftreten. Daf&uuml;r sollen
              neben der Anzahl der Aufnahmen von, etablierte Laborparameter und
              Medikation in die Analyse mit eingebunden werden. <br>
              Ziel 2:
              Wetter-bedingtes exzessives Risiko f&uuml;r Schlaganfall <br> Modellierung
              des exzessiven (wetter-bedingten) Risikos f&uuml;r die Anzahl der
