            <h6>
              Wissenschaftlicher Hintergrund
            </h6>
            <br>
            <p class="big">
              Die Vorhersage der Anzahl neurovaskul&auml;rer Notf&auml;lle w&auml;hrend des Bereitschaftsdienstes von Neurointerventionalisten ist eine komplexe Aufgabe, die f&uuml;r die Ressourcenzuteilung f&uuml;r mehrere Abteilungen, einschlie&szlig;lich der Intensivstation, von gro&szlig;er Bedeutung ist. Es wird ein starker Zusammenhang zwischen Wetterfronten und dem sympathischen Nervensystem sowie dem Risiko, einen Schlaganfall zu erleiden, vermutet1,2.
              <br> <br>
              Schlaganfall ist ein Oberbegriff, der drei Hauptunterarten umfasst: 
              <br>
              1. Die h&auml;ufigste Form ist der isch&auml;mische Schlaganfall (80-90 %), der durch Blutgerinnsel (Thromben) gekennzeichnet ist, die die Blutversorgung des Gehirns blockieren.
              <br>
              2. Eine intrakranielle Blutung (ICH) tritt in 10-15 % der F&auml;lle auf, wenn geschw&auml;chte intrakranielle Gef&auml;&szlig;e platzen und dadurch eine Blutung innerhalb des Hirnparenchyms verursacht wird, vor allem aufgrund von Bluthochdruck.
              <br>
              3. In den verbleibenden ~5 % der F&auml;lle wird die Subarachnoidalblutung (SAB) durch Blutungen an der Hirnoberfl&auml;che verursacht, in der Regel durch die Ruptur einer intrakraniellen Gef&auml;&szlig;wandausbuchtung (Aneurysma). Die SAB betrifft vor allem junge Patientinnen und Patienten und hat eine sehr hohe 30-Tage-Sterblichkeitsrate von bis zu 40-50 %.
              <br> <br>
              Vorangegangene Studien haben einen Zusammenhang zwischen Wetterfronten und dem Schlaganfallrisiko f&uuml;r alle Subtypen gezeigt, allerdings wurden dabei nur explorative Analysen ohne geeignete Validierungs- und Testgruppen durchgef&uuml;hrt und m&ouml;gliche individuelle Risikofaktoren nicht ber&uuml;cksichtigt3,4. Daher m&ouml;chten wir unser Projekt auf mehrere Standorte innerhalb der Medizininformatik-Initiative ausweiten und dabei einen prognose-orientierten Analyseansatz verwenden, um die klinische Ressourcenplanung zu unterst&uuml;tzen und die Patientenversorgung zu optimieren.
              <br> <br>
              Aufgrund der 2015 ver&ouml;ffentlichten wegweisenden Studie (MR CLEAN)7 kam es zu einer deutlichen Verschiebung der (isch&auml;mischen) Schlaganfalltherapie hin zu endovaskul&auml;ren Kathetereingriffen bei vermuteten gro&szlig;en Gef&auml;&szlig;verschl&uuml;ssen (LVO). Auf MR CLEAN folgte eine Vielzahl randomisierter kontrollierter Studien (RCT), in denen die Bedeutung schneller Gef&auml;&szlig;rekanalisationen bei LVOs betont wurde. Dies hat zu einem Anstieg der Schlaganfallf&auml;lle gef&uuml;hrt, die in Kliniken der terti&auml;ren Versorgung vorgestellt werden. Daher konzentrieren wir uns auf F&auml;lle ab 2015 bis dato.
              <br>
              Die Machbarkeit dieser Abfragen und Modellierung haben wir f&uuml;r das Universit&auml;tsklinikum Mannheim (MIRACUM) bereits gezeigt5,6.
            </p>
